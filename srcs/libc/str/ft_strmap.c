#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*s2;
	int		i;

	if (!s)
		return (NULL);
	if (!(s2 = (char*)malloc(sizeof(char) * ft_strlen(s) + 1)))
		return (0);
	i = -1;
	while (s[++i])
		s2[i] = f(s[i]);
	s2[i] = '\0';
	return (s2);
}
