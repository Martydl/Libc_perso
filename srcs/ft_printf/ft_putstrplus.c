#include "ft_printf.h"

void	ft_putstrplus(char *s, t_prin *prin)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
	prin->ret += i;
}
